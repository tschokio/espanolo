export function localizedLessonSummary(lesson, nativeLanguage = "de") {
  if (nativeLanguage !== "de") return lesson?.summary || "";
  const title = localizedLessonTitle(lesson, nativeLanguage);
  const checkpoint = lesson?.isCheckpoint || /checkpoint/i.test(`${lesson?.slug || ""} ${lesson?.title || ""}`);
  if (checkpoint) {
    return `In diesem Checkpoint zeigst du, dass du „${title}“ sicher unterscheiden und ohne Vorlage auf Spanisch abrufen kannst.`;
  }
  return `Du lernst gezielt „${title}“: erst Bedeutung und Entscheidung verstehen, dann das spanische Muster selbst bilden und aktiv abrufen.`;
}

